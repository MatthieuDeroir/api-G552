const Frame_0xA9 = require('../../../Data/Frame/Frame_0xA9');


describe('Frame_0xA9', () => {
    test('build returns correct object', () => {
        // Create a sample 54-byte message
        const message = new Uint8Array([
            0x40, 0x09, 0x3D, 0x30, 0x31, 0x20, 0x32, 0x30, 0x30, 0x31, 0x20, 0x30,
            0x30, 0x20, 0x30, 0x30, 0x30, 0x30, 0x31, 0x35, 0x20, 0x30, 0x30, 0x20,
            0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30,
            0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30,
            0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30
        ]);

        // Build the expected object
        const expectedGSI = {
            insertType: 'DirectConsoleData',
            Home_PlayerNumber: [-3, 1, -2, 2001, 0, 0, -3, 15, 0, 0, 0, 0, 0, 0, 0, 0]
        };

        // Call the build function and compare the result with the expected object
        expect(Frame_0xA9.build(message)).toEqual(expectedGSI);
    });
});
